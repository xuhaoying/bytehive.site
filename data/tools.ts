// For backward compatibility
export const getAllCategories = (): ToolCategory[] => {
  const categoriesSet = new Set<ToolCategory>();
  
  tools.forEach(tool => {
    categoriesSet.add(tool.category);
  });
  
  return Array.from(categoriesSet).sort();
};

export const getAllPaymentModels = (): PaymentModel[] => {
  const paymentModelsSet = new Set<PaymentModel>();
  
  tools.forEach(tool => {
    paymentModelsSet.add(tool.pricing);
  });
  
  return Array.from(paymentModelsSet).sort();
};

export const getAllTags = (): string[] => {
  const tagsSet = new Set<string>();
  
  tools.forEach(tool => {
    tool.tags?.forEach(tag => {
      tagsSet.add(tag);
    });
  });
  
  return Array.from(tagsSet).sort();
};

export const getToolsByCategory = (category: ToolCategory): Tool[] => {
  return tools.filter(tool => tool.category === category);
};

export const getToolBySlug = (slug: string): Tool | undefined => {
  return tools.find(tool => tool.slug === slug);
};

export const getFeaturedTools = (limit: number = 8): Tool[] => {
  return tools
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, limit);
};

export const searchTools = (query: string): Tool[] => {
  const lowercaseQuery = query.toLowerCase();
  return tools.filter(tool => 
    tool.name.toLowerCase().includes(lowercaseQuery) ||
    tool.description.toLowerCase().includes(lowercaseQuery) ||
    tool.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getToolsByPricing = (pricing: PaymentModel): Tool[] => {
  return tools.filter(tool => tool.pricing === pricing);
};

export const getToolsBySubcategory = (subcategory: string): Tool[] => {
  return tools.filter(tool => tool.subcategory === subcategory);
};

export const getPopularTools = (minPopularity: number = 80): Tool[] => {
  return tools
    .filter(tool => tool.popularity >= minPopularity)
    .sort((a, b) => b.popularity - a.popularity);
};

export const getTotalToolsCount = (): number => {
  return tools.length;
};

export const getToolsCountByCategory = (): Record<ToolCategory, number> => {
  const counts: Record<ToolCategory, number> = {
    'deployment': 0,
    'development': 0,
    'ai-tools': 0,
    'design': 0,
    'marketing': 0,
    'database': 0,
    'monitoring': 0,
    'learning': 0
  };
  
  tools.forEach(tool => {
    counts[tool.category]++;
  });
  
  return counts;
};

